
DROP TABLE IF EXISTS TBL_REFERENCE;
CREATE TABLE TBL_REFERENCE (
    ID_REFERENCE INT AUTO_INCREMENT PRIMARY KEY,
    FULL_NAME VARCHAR(50) NOT NULL,
    FULL_ADDRESS VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(30) NOT NULL,
    BUSINESS_OCCUPATION VARCHAR(100) NOT NULL
 );

DROP TABLE IF EXISTS TBL_PUBLICATION;
CREATE TABLE TBL_PUBLICATION (
    ID_PUBLICATION INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(200) NOT NULL,
    PUBLISHER VARCHAR(200) NOT NULL,
    DATE_PUBLISHED DATE NOT NULL,
    CITY_PUBLISHED VARCHAR(40) NOT NULL,
    COUNTRY VARCHAR(100) NOT NULL
 );

DROP TABLE IF EXISTS TBL_RELATIVE;
CREATE TABLE TBL_RELATIVE (
    ID_RELATIVE INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
    RELATIONSHIP VARCHAR(20) NOT NULL,
    INTERNATIONAL_ORGANIZATION VARCHAR(200) NOT NULL
 );



